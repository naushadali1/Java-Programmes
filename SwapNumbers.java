public class SwapNumbers {
    public static void main(String[] args) {
        int a = 20, b = 10, temp;
        System.out.print("Before swaping the values are ");
        System.out.print(a + " ");
        System.out.println(b);
        temp = a;
        a = b;
        b = temp;
        System.out.print("After swaping the values are ");
        System.out.print(a + " ");
        System.out.print(b);

    }
}
